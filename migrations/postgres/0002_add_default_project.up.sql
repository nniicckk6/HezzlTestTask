-- Миграция 0002 (up): вставка дефолтного проекта 'Первая запись', если он еще не существует
-- Подробное описание: проверяем отсутствие проекта с таким именем и вставляем новую запись в таблицу Projects.

INSERT INTO Projects (name)
SELECT 'Первая запись'
WHERE NOT EXISTS (
    SELECT 1 FROM Projects WHERE name = 'Первая запись'
);
